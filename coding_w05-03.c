#include <stdio.h>  // เรียกใช้ไลบรารีมาตรฐานสำหรับฟังก์ชัน printf

int main() {
    // บรรทัดที่ 1: แสดงตัวแปร a = 0.617%
    printf("a = 0.617%%\n");  // %% ใช้เพื่อแสดงเครื่องหมาย %

    // บรรทัดที่ 2: แสดงตัวแปร b = 25%
    printf("b = 25%%\n");

    // บรรทัดที่ 3: แสดงตัวแปร c = 1.9000%
    printf("c = 1.90000%%\n");

    // บรรทัดที่ 4: แสดงตัวแปร d = 9%
    printf("d = 9%%\n");

    // บรรทัดที่ 5: แสดงตัวแปร e = 124.555%
    printf("e = 124.555%%\n");

    // บรรทัดที่ 6: แสดงตัวแปร f = 0.001%
    printf("f = 0.001%%\n");

    return 0;  // จบโปรแกรม
}